public class News2view {

    String post ;
    String isco;
    String name;

    public News2view(){}

    public String getPost() {
        return post;
    }

    public void setPost(String post) {
        this.post = post;
    }

    public String getIsco() {
        return isco;
    }

    public void setIsco(String isco) {
        this.isco = isco;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
